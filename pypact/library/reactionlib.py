def getreaction(mt, proj=PROJECTILE_NEUTRON):
    return "({},{:5})".format(get_projectile_symbol(proj), REACTION_DICTIONARY[mt])


def getmt(reactionstring, fullstring=False):
    """
    Returns -1 if not found
    """
    mt = -1
    for k, v in REACTION_DICTIONARY.items():
        rstr = reactionstring
        if fullstring:
            # strip off projectile and brackets
            rstr = reactionstring[3:-1].strip()
        if v.strip() == rstr:
            mt = k
    return mt
